package com.example.project;

public class AssignmentResult {
    private String name;
    private int grade;
    private int weight;
    private boolean received;

    public String getName() {
        return name;
    }

    public int getGrade() {
        return grade;
    }

    public int getWeight() {
        return weight;
    }

    public boolean isReceived() {
        return received;
    }
}